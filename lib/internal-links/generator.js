// Auto-generate Internal Links Utility
// lib/internal-links/generator.js
// Automatically create internal links for movie → actor, movie → genre, etc.

import InternalLink from "../../model/InternalLink";
import Article from "../../model/article";
import Celebrity from "../../model/celebrity";

/**
 * Generate links for a movie to its cast
 * movie → actor (80% SEO weight)
 */
export async function generateMovieToCastLinks(movieId, movieSlug, cast) {
  try {
    const links = [];

    for (const castMember of cast || []) {
      // Find celebrity by name
      const celebrity = await Celebrity.findOne({
        "heroSection.name": new RegExp(castMember.name, "i"),
      });

      if (celebrity) {
        const link = await InternalLink.create({
          sourceId: movieId,
          sourceType: "Article",
          sourceSlug: movieSlug,
          targetId: celebrity._id,
          targetType: "Celebrity",
          targetSlug: celebrity.slug,
          linkType: "movie-to-actor",
          anchorText: `${castMember.name} movies`,
          section: "content",
          seoWeight: 80, // High weight for cast links
          isAutoGenerated: true,
        });

        links.push(link);
      }
    }

    return links;
  } catch (error) {
    console.error("Error generating movie-to-cast links:", error);
    return [];
  }
}


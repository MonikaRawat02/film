/**
 * Generate all internal links for a movie
 * Comprehensive linking: cast + similar + sub-pages
 */
export async function generateAllMovieLinks(movieId, movieSlug, movieData) {
  try {
    const allLinks = [];

    // Generate cast links
    const castLinks = await generateMovieToCastLinks(
      movieId,
      movieSlug,
      movieData.cast
    );
    allLinks.push(...castLinks);

    // Generate similar movie links
    const similarLinks = await generateMovieToSimilarLinks(
      movieId,
      movieSlug,
      movieData.relatedMovies
    );
    allLinks.push(...similarLinks);

    return {
      success: true,
      linksCreated: allLinks.length,
      details: {
        castLinks: castLinks.length,
        similarMovieLinks: similarLinks.length,
      },
      data: allLinks,
    };
  } catch (error) {
    console.error("Error generating all movie links:", error);
    return {
      success: false,
      message: error.message,
    };
  }
}

/**
 * Get internal links for rendering in templates
 * Returns links with proper context for template rendering
 */
export async function getLinksForRendering(sourceId, sourceType) {
  try {
    const links = await InternalLink.getOutgoingLinks(sourceId, sourceType);

    // Group by link type
    const grouped = {
      "movie-to-actor": links.filter((l) => l.linkType === "movie-to-actor"),
      "movie-to-similar": links.filter((l) => l.linkType === "movie-to-similar"),
      "page-to-page": links.filter((l) => l.linkType === "page-to-page"),
      "actor-to-movies": links.filter((l) => l.linkType === "actor-to-movies"),
    };

    return {
      success: true,
      totalLinks: links.length,
      grouped,
      all: links,
    };
  } catch (error) {
    console.error("Error getting links for rendering:", error);
    return {
      success: false,
      message: error.message,
    };
  }
}
